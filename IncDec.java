public class IncDec {
    public static void main(String[] args) {

        int x =5 ;
        x++;
        System.out.println(x);

        int y =5 ;
        ++y;
        System.out.println(y);

//pre increment operator
        int c =5,d ;
        d=++c;
        System.out.println(c);
        System.out.println(d);


        int p=5 ,r=4,z;
        z=2*p++ + 3*++r;
        System.out.println(z);




    }
}
